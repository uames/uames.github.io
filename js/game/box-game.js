  function imgPreload(srcs,callback){
  	var count = 0,imgNum = 0,images = {};

  	for(src in srcs){
  		imgNum++;
  	}
  	for(src in srcs ){
  		images[src] = new Image();
  		images[src].onload = function(){
  			//
  			if (++count >= imgNum)
  			{
  				callback(images);
  			}
  		}
  		images[src].src = srcs[src];
  	}
  }
